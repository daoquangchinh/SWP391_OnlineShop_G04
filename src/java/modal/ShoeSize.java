package modal;

/**
 *
 * @author ViQuan
 */
public class ShoeSize {

    private int id;
    private int size;

    public ShoeSize() {
    }

    public ShoeSize(int id, int size) {
        this.id = id;
        this.size = size;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    @Override
    public String toString() {
        return "ShoeSize{" + "id=" + id + ", size=" + size + '}';
    }

}
